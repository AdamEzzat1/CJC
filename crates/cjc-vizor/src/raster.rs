#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_pixel_buffer_new() {
        let buf = PixelBuffer::new(10, 10, Color::WHITE);
        assert_eq!(buf.data.len(), 10 * 10 * 4);
        assert_eq!(buf.data[0], 255); // R
    }

    #[test]
    fn test_set_pixel_bounds() {
        let mut buf = PixelBuffer::new(10, 10, Color::WHITE);
        buf.set_pixel(-1, -1, Color::BLACK); // should not crash
        buf.set_pixel(100, 100, Color::BLACK); // should not crash
        buf.set_pixel(5, 5, Color::BLACK);
        let idx = (5 * 10 + 5) * 4;
        assert_eq!(buf.data[idx], 0);
    }

    #[test]
    fn test_rasterize_deterministic() {
        let scene = crate::scene::Scene::new(100, 100);
        let buf1 = rasterize(&scene);
        let buf2 = rasterize(&scene);
        assert_eq!(buf1.data, buf2.data);
    }
}
