//! Shared rasterization: scene → pixel buffer.
//!
//! Used by both BMP and PNG exporters. Fully deterministic.
//! Uses integer arithmetic (Bresenham) for lines.

use crate::color::Color;
use crate::scene::{Scene, SceneElement, TextAnchor};

/// A pixel buffer (RGBA, row-major, top-to-bottom).
pub struct PixelBuffer {
    pub width: u32,
    pub height: u32,
    /// RGBA pixels, row-major, length = width * height * 4.
    pub data: Vec<u8>,
}

impl PixelBuffer {
    pub fn new(width: u32, height: u32, bg: Color) -> Self {
        let len = (width as usize) * (height as usize) * 4;
        let mut data = vec![0u8; len];
        for i in 0..(width as usize * height as usize) {
            data[i * 4] = bg.r;
            data[i * 4 + 1] = bg.g;
            data[i * 4 + 2] = bg.b;
            data[i * 4 + 3] = bg.a;
        }
        PixelBuffer { width, height, data }
    }

    /// Set a pixel. Clips to bounds.
    #[inline]
    pub fn set_pixel(&mut self, x: i32, y: i32, color: Color) {
        if x < 0 || y < 0 || x >= self.width as i32 || y >= self.height as i32 {
            return;
        }
        let idx = (y as usize * self.width as usize + x as usize) * 4;
        if color.a == 255 {
            self.data[idx] = color.r;
            self.data[idx + 1] = color.g;
            self.data[idx + 2] = color.b;
            self.data[idx + 3] = 255;
        } else {
            // Alpha blend.
            let a = color.a as u16;
            let inv_a = 255 - a;
            self.data[idx] = ((color.r as u16 * a + self.data[idx] as u16 * inv_a) / 255) as u8;
            self.data[idx + 1] = ((color.g as u16 * a + self.data[idx + 1] as u16 * inv_a) / 255) as u8;
            self.data[idx + 2] = ((color.b as u16 * a + self.data[idx + 2] as u16 * inv_a) / 255) as u8;
            self.data[idx + 3] = 255;
        }
    }

    /// Fill a rectangle.
    pub fn fill_rect(&mut self, x: i32, y: i32, w: i32, h: i32, color: Color) {
        for dy in 0..h {
            for dx in 0..w {
                self.set_pixel(x + dx, y + dy, color);
            }
        }
    }

    /// Fill a circle.
    pub fn fill_circle(&mut self, cx: i32, cy: i32, r: i32, color: Color) {
        let r2 = r * r;
        for dy in -r..=r {
            for dx in -r..=r {
                if dx * dx + dy * dy <= r2 {
                    self.set_pixel(cx + dx, cy + dy, color);
                }
            }
        }
    }

    /// Draw a line using Bresenham's algorithm.
    pub fn draw_line(&mut self, x0: i32, y0: i32, x1: i32, y1: i32, color: Color, width: i32) {
        if width <= 1 {
            self.bresenham(x0, y0, x1, y1, color);
        } else {
            // Thick line: draw Bresenham with offset perpendicular lines.
            let half = width / 2;
            let dx = (x1 - x0).abs();
            let dy = (y1 - y0).abs();
            if dx >= dy {
                for offset in -half..=half {
                    self.bresenham(x0, y0 + offset, x1, y1 + offset, color);
                }
            } else {
                for offset in -half..=half {
                    self.bresenham(x0 + offset, y0, x1 + offset, y1, color);
                }
            }
        }
    }

    fn bresenham(&mut self, mut x0: i32, mut y0: i32, x1: i32, y1: i32, color: Color) {
        let dx = (x1 - x0).abs();
        let dy = -(y1 - y0).abs();
        let sx = if x0 < x1 { 1 } else { -1 };
        let sy = if y0 < y1 { 1 } else { -1 };
        let mut err = dx + dy;

        loop {
            self.set_pixel(x0, y0, color);
            if x0 == x1 && y0 == y1 { break; }
            let e2 = 2 * err;
            if e2 >= dy {
                if x0 == x1 { break; }
                err += dy;
                x0 += sx;
            }
            if e2 <= dx {
                if y0 == y1 { break; }
                err += dx;
                y0 += sy;
            }
        }
    }
}

// ── Built-in bitmap font (6x10 ASCII) ──────────────────────────────

/// Render a character from the built-in bitmap font.
/// Returns true if the character was rendered.
pub fn render_char(buf: &mut PixelBuffer, ch: char, x: i32, y: i32, color: Color) -> bool {
    let idx = ch as usize;
    if idx < 32 || idx > 126 { return false; }
    let glyph = &FONT_DATA[idx - 32];
    for (row, &bits) in glyph.iter().enumerate() {
        for col in 0..6 {
            if bits & (1 << (5 - col)) != 0 {
                buf.set_pixel(x + col, y + row as i32, color);
            }
        }
    }
    true
}

/// Render a string using the built-in bitmap font.
pub fn render_text(buf: &mut PixelBuffer, text: &str, x: i32, y: i32, color: Color) {
    let mut cx = x;
    for ch in text.chars() {
        render_char(buf, ch, cx, y, color);
        cx += 6; // 6-pixel wide font
    }
}

/// Rasterize a scene to a pixel buffer.
pub fn rasterize(scene: &Scene) -> PixelBuffer {
    let mut buf = PixelBuffer::new(scene.width, scene.height, Color::WHITE);

    for elem in &scene.elements {
        match elem {
            SceneElement::Rect { x, y, w, h, fill, stroke, stroke_width } => {
                buf.fill_rect(*x as i32, *y as i32, *w as i32, *h as i32, *fill);
                if let Some(s) = stroke {
                    let sw = (*stroke_width).max(1.0) as i32;
                    // Top edge.
                    buf.fill_rect(*x as i32, *y as i32, *w as i32, sw, *s);
                    // Bottom edge.
                    buf.fill_rect(*x as i32, (*y + *h - *stroke_width) as i32, *w as i32, sw, *s);
                    // Left edge.
                    buf.fill_rect(*x as i32, *y as i32, sw, *h as i32, *s);
                    // Right edge.
                    buf.fill_rect((*x + *w - *stroke_width) as i32, *y as i32, sw, *h as i32, *s);
                }
            }
            SceneElement::Circle { cx, cy, r, fill, stroke: _ } => {
                buf.fill_circle(*cx as i32, *cy as i32, *r as i32, *fill);
                // Stroke circle not implemented in v0 (just fill).
            }
            SceneElement::Line { x1, y1, x2, y2, stroke, width } => {
                buf.draw_line(*x1 as i32, *y1 as i32, *x2 as i32, *y2 as i32, *stroke, *width as i32);
            }
            SceneElement::Polyline { points, stroke, width, .. } => {
                for pair in points.windows(2) {
                    buf.draw_line(
                        pair[0].0 as i32, pair[0].1 as i32,
                        pair[1].0 as i32, pair[1].1 as i32,
                        *stroke, *width as i32,
                    );
                }
            }
            SceneElement::Text { x, y, text, font_size, fill, anchor, rotation: _ } => {
                // Scale factor for bitmap font (base is 10px).
                let _scale = (*font_size / 10.0).max(1.0);
                let text_w = text.len() as i32 * 6;
                let offset_x = match anchor {
                    TextAnchor::Start => 0,
                    TextAnchor::Middle => -text_w / 2,
                    TextAnchor::End => -text_w,
                };
                // For simplicity, render at 1x scale (no rotation in raster v0).
                render_text(&mut buf, text, *x as i32 + offset_x, *y as i32 - 10, *fill);
            }
        }
    }

    buf
}

