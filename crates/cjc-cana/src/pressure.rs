#[cfg(test)]
mod tests {
    use super::*;
    use crate::features::extract;
    use cjc_mir::{MirBody, MirFnId, MirFunction};

    fn empty_fn(name: &str) -> MirFunction {
        MirFunction {
            id: MirFnId(0),
            name: name.to_string(),
            type_params: vec![],
            params: vec![],
            return_type: None,
            body: MirBody {
                stmts: vec![],
                result: None,
            },
            is_nogc: false,
            cfg_body: None,
            decorators: vec![],
            vis: cjc_ast::Visibility::Public,
            local_count: 0,
        }
    }

    fn make_program(fn_names: &[&str]) -> MirProgram {
        MirProgram {
            functions: fn_names.iter().map(|n| empty_fn(n)).collect(),
            struct_defs: vec![],
            enum_defs: vec![],
            entry: MirFnId(0),
        }
    }

    #[test]
    fn null_predictor_thermal_is_zero_everywhere() {
        let p = make_program(&["a", "b", "c"]);
        let f = extract(&p);
        let pp = NullPressurePredictor;
        let t = pp.predict_thermal(&p, &f);
        assert_eq!(t.len(), 3);
        for v in t.values() {
            assert_eq!(*v, 0.0);
        }
    }

    #[test]
    fn null_predictor_memory_is_zero_everywhere() {
        let p = make_program(&["a", "b"]);
        let f = extract(&p);
        let pp = NullPressurePredictor;
        let m = pp.predict_memory_peak(&p, &f);
        assert!(m.values().all(|&v| v == 0.0));
    }

    #[test]
    fn null_predictor_cpu_is_zero_everywhere() {
        let p = make_program(&["main"]);
        let f = extract(&p);
        let pp = NullPressurePredictor;
        let c = pp.predict_cpu_saturation(&p, &f);
        assert_eq!(c.get("main"), Some(&0.0));
    }

    #[test]
    fn null_predictor_finds_no_hot_kernels() {
        let p = make_program(&["a", "b"]);
        let f = extract(&p);
        let pp = NullPressurePredictor;
        let hot = pp.identify_structural_hot_kernels(&p, &f);
        assert!(hot.is_empty());
    }

    #[test]
    fn null_predictor_identifies_itself_correctly() {
        let pp = NullPressurePredictor;
        assert_eq!(pp.name(), "null");
        assert_eq!(pp.version(), 0);
    }

    #[test]
    fn null_predictor_output_is_deterministic() {
        let p = make_program(&["a", "b", "c"]);
        let f = extract(&p);
        let pp = NullPressurePredictor;
        let first = pp.predict_thermal(&p, &f);
        for _ in 0..50 {
            let again = pp.predict_thermal(&p, &f);
            assert_eq!(first, again);
        }
    }

    #[test]
    fn null_predictor_map_uses_btreemap_ordering() {
        // Insert programs out-of-alphabetical-order; predictor returns
        // them in BTreeMap-sorted order regardless.
        let p = make_program(&["zebra", "alpha", "middle"]);
        let f = extract(&p);
        let pp = NullPressurePredictor;
        let t = pp.predict_thermal(&p, &f);
        let keys: Vec<&str> = t.keys().map(|s| s.as_str()).collect();
        assert_eq!(keys, vec!["alpha", "middle", "zebra"]);
    }
}
